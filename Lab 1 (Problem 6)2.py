x = int(input("Enter an integer: "))
y = int(input("Enter an integer: "))

sum = x + y
print("The sum of x and y is: " + str(sum))

subtract = x - y
print ("The difference of x and y is: " + str(subtract))

product = x * y
print ("The product of x and y is: " + str(product))

quotient = x / y
print("The quotient of x and y is: " + str(quotient))

modulus = x % y
print("The remainder of dividing x by y is: " + str(modulus))

exponent = x**y
print("The result when x is raised to the power of y is: " + str(exponent))
